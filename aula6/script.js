//declarativas

function funcao() {
    console.log('Sou uma mensagem de funcao declarativa!')
}

funcao();

// expressao de funcao - e opcional a funcao ter nome ou nao nesse caso pq se nao houver, ela adota o nome da variavel

var funcao = function () {
    console.log('Sou uma mensagem de funcao de expressao!')
}

funcao();

// arrow function
var funcao = () => {
    console.log('Sou uma arrow function')
}

funcao();

